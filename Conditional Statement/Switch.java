public class Switch {
    public static void main(String[] args) {
        int a = 2;
        switch(a){
            case 1: System.out.println("tea");
            break;
            case 2: System.out.println("coffe");
            break;
            case 3: System.out.println("capatino");
            break;
            default: System.out.println("milk");
        }
    }
}
